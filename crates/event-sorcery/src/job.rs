    #[tokio::test]
    async fn flush_writes_an_apalis_compatible_pending_row() {
        let pool = jobs_pool().await;

        let mut queue = JobQueue::<crate::jobs![SendEmail]>::default();
        queue.push(SendEmail::Welcome {
            address: "a@example.com".to_string(),
        });

        let mut transaction = pool.begin().await.unwrap();
        flush_jobs(&mut transaction, queue).await.unwrap();
        transaction.commit().await.unwrap();

        let (payload, job_type, status): (Vec<u8>, String, String) =
            sqlx::query_as("SELECT job, job_type, status FROM Jobs")
                .fetch_one(&pool)
                .await
                .unwrap();

        assert_eq!(job_type, SendEmail::KIND);
        assert_eq!(status, "Pending");
        let decoded: SendEmail = serde_json::from_slice(&payload).unwrap();
        assert!(matches!(decoded, SendEmail::Welcome { address } if address == "a@example.com"));
    }

    #[tokio::test]
    async fn flush_writes_distinct_rows_for_multiple_job_types() {
        let pool = jobs_pool().await;

        let mut queue = JobQueue::<crate::jobs![SendEmail, ChargeCard]>::default();
        queue.push(SendEmail::Welcome {
            address: "a@example.com".to_string(),
        });
        queue.push(ChargeCard { cents: 4200 });

        let mut transaction = pool.begin().await.unwrap();
        flush_jobs(&mut transaction, queue).await.unwrap();
        transaction.commit().await.unwrap();

        let rows: Vec<(String,)> = sqlx::query_as("SELECT job_type FROM Jobs ORDER BY job_type")
            .fetch_all(&pool)
            .await
            .unwrap();

        let job_types: Vec<String> = rows.into_iter().map(|(job_type,)| job_type).collect();
        assert_eq!(job_types.len(), 2);
        assert!(job_types.contains(&ChargeCard::KIND.to_string()));
        assert!(job_types.contains(&SendEmail::KIND.to_string()));
    }
